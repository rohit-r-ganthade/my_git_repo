public class Increment {

    public static void main(String args[]) {

        int tuna = 5;
        System.out.println(tuna);

        tuna++;
        System.out.println(tuna);

        tuna--;
        System.out.println(tuna);

        tuna = tuna + 10;
        System.out.println(tuna);

        tuna += 10;
        System.out.println(tuna);

        System.out.println(++tuna);
        System.out.println(--tuna);

    }
}
