public class MathClassMethods {

    public static void main(String args[]) {

        System.out.println(Math.abs(-21.3));
        System.out.println(Math.abs(21.3));
        System.out.println(Math.abs(0));
        System.out.println(Math.ceil(15.1));
        System.out.println(Math.floor(18.9));
        System.out.println(Math.max(21.1, 23.4));
        System.out.println(Math.min(4,8));
        System.out.println(Math.pow(2,4));
        System.out.println(Math.random());
        System.out.println(Math.round(3.49));

    }
}